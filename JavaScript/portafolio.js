function mostrarTaller() {
    var taller = document.getElementById("lista").value;
    if (taller == "Taller de condicionales") {
        document.getElementById("taller2").style.display = "none";
        document.getElementById("taller3").style.display = "none";
        document.getElementById("taller").style.display = "block";
    }
    if (taller == "Taller de ciclos") {
        document.getElementById("taller").style.display = "none";
        document.getElementById("taller3").style.display = "none";
        document.getElementById("taller2").style.display = "block";
    }
    if (taller == "Taller de arreglos") {
        document.getElementById("taller").style.display = "none";
        document.getElementById("taller2").style.display = "none";
        document.getElementById("taller3").style.display = "block";
    }
}