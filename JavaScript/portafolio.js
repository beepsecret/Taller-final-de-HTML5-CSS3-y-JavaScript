function mostrarCodigo() {
    document.getElementById("codigo1").style.display = "block";
    var boton = document.getElementById("button");
    console.log(boton.innerText);
    if (boton.innerText == "Mostrar código") {
        boton.innerText = "Ocultar código";
    } else {
        document.getElementById("codigo1").style.display = "none";
        boton.innerText = "Mostrar código";
    }
    console.log(boton.innerText);
}

